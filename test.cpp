#include<iostream>
 
using namespace std;
 
int main()
{
    cout<<"Addition of two numbers" << endl;

    int a, b, c;
    a = 10;
    b = 20;
    c = a + b;
    cout<< " " << a << " + " << b << " is " << c << endl;

    cout<<"Subtraction of two numbers" << endl;

    int d, e, f;
    d = 20;
    e = 2;
    f = d - e;
    cout<< " " << d << " - " << e << " is " << f << endl;

    return 0;
}